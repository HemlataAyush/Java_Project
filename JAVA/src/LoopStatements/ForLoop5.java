package LoopStatements;

public class ForLoop5 {

	public static void main(String[] args) {

      // ouput 1 3 5 7 ... 20
		
		//starting condition - 1
		//ending condition - 20
		// inc/dec - i=i+2;
		
		for(int i = 1; i<=20; i=i+2) {
			System.out.println(i);
		}

		System.out.println("Program completed");
		
	}

}
