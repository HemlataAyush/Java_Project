package LoopStatements;

public class ForLoop3 {

	public static void main(String[] args) {
		// WAP print 5 to 20 number using for loop
		// Starting code 5
		//Ending code 20
		//i++
		
		// 5<= 20 - True
		// 6<=20 - true
		//  -
		//  -
		// 20<=20 - true
		// 21<=20 - false
		for(int i = 5; i <= 20; i++) {
			System.out.println(i);
		}

	}

}
